    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        ConnectionManager conMan = new ConnectionManager();
        Connection con = conMan.logOn();
        String query = "select * from admin where id ='"+txtId.getText()+"';";
        try{
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery(query);
            if(rs.next()) {
                if (rs.getString("password").equals(txtPass.getText())) {
                    FormMenuAdmin fMA = new FormMenuAdmin(new Admin(txtId.getText(),
                                        rs.getString("nama"), txtPass.getText()));
                    fMA.setVisible(true);
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Selamat Datang Administrator " + 
                                              rs.getString("nama"));
            }else {
                JOptionPane.showMessageDialog(null, "Login Gagal");
            }
        }
        }catch (SQLException ex){
            ex.printStackTrace();
        }
        conMan.logOff();
    }//GEN-LAST:event_btnLoginActionPerformed

