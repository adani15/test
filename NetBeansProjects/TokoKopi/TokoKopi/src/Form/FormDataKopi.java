        int stok = Integer.parseInt(tblKopi.getValueAt(row, 2).toString());
        int jumlah = Integer.parseInt(txtTambahStok.getText());
        String id = tblKopi.getValueAt(row, 0).toString();
        ek.restoreStok(id, stok, jumlah);
        loadTabelKopi();
