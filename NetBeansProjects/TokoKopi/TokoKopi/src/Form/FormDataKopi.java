    private void loadTabelKopi() {
        myKp = ek.getAllKopi();
        Object[][] dtKp = new Object[myKp.size()][4];
        int mySize = 0;
        for (Kopi kopi : myKp) {
            dtKp[mySize][0] = kopi.getIdKopi();
            dtKp[mySize][1] = kopi.getJenisKopi();
            dtKp[mySize][2] = kopi.getStokKopi();
            dtKp[mySize][3] = kopi.getHargaKopi();
            mySize++;
        }
        tblKopi.setModel(new javax.swing.table.DefaultTableModel(dtKp,
                new String[]{"ID Kopi", "Jenis Kopi", "Stok", "Harga"}));

        jScrollPane1.setViewportView(tblKopi);
        
    }

